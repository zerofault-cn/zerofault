<h1>授权条款同意书</h1>
 <h2>特别提示</h2>
 <h2>在您看到本授权条款同意书时，您在报名参加“博客网.第二届美女博客大赛”评选活动（下称“活动”），将成为活动选手之一。请您认真仔细阅读以下条款，这将是活动对您的要求，也是您对活动的承诺。如果您完全同意以下条款并承诺遵守，请您确认“同意”。以下规则具备法律效力，请详细阅读。</h2>
 1、选手已了解活动主旨、报名要求，报名目的健康向上，并遵守活动组委会关于活动的一切合法规章制度和要求。 <br>
2、选手向活动组委会提供的全部资料均真实、合法。 <br>
3、选手的姓名、照片、陈述等一切个人及参赛资料均有可能在媒体的本活动广告或节目中出现，包括（不仅限于）电视、报刊、杂志、广播及互联网等；以上这些资料的使用将取决于活动组委会，并可永久使用。 <br>
4、选手应自行保管活动组委会提供的博客链接及密码，因链接、密码被盗、遗失、被破坏等造成选手参与活动受阻的，组委会不承担任何责任。 <br>
5、选手在参赛博客中登载的内容（文字、图片、DV等）不得违反我国法律法规及网站规定，如因内容违法或违规，导致内容被删除、博客被取消或引起其他纠纷的，选手应自行承担由此所产生的全部责任。 <br>
6、选手在博客中除上述情形外，如出现组委会认为其他应删除的内容，组委会可告知选手自行删除或直接删除；如选手此类行为超过两次，组委会将有权取消选手的参赛资格。 <br>
7、选手在整个活动的参选期间，组委会将有权对选手随时进行筛选核对，如发现有虚假不实等情形，组委会将取消选手资格，由序位靠前的选手顶替晋级。 <br>
8、选手通过预选阶段最终进入决选前，必须参加由组委会安排培训、包装课程。 <br>
9、此次活动的培训包装课程将采取半封闭式，选手应遵守以下要求： <br>
（1）完全按照组委会的通知要求参加培训，如拒绝或不按时参加者，组委会有权取消其参赛资格；<br> 
（2）在培训期间，选手将接受高强度的训练、排练及摄影，选手应保证良好的身体健康状况；如选手身体状况无法适应者，应提前如实告知组委会；无法完成培训的选手，组委会将取消选手资格，由序位靠前的选手顶替进入决选。 <br>
（3）在培训期间，选手应服从组委会的全部安排；严禁选手在培训时间段内擅自行动、擅自外出； <br>
（4）所有选手应互相帮助、互相尊重、友好相处； <br>
（5）如有违反规定者，组委会将予以批评警告；如不听警告者，组委会将取消其参赛资格； <br>
（6）选手应自行承担在培训期间内所发生事故的全部法律后果及责任，但因组委会原因导致的除外。 <br>
10、进入决选的选手，应服从组委会的全部活动安排；未经组委会同意，不参加组委会安排之外的商业活动，不与任何团体签约作宣传活动，不以“博客网第二届美女博客大赛”之名衔对外活动。 <br>
11、进入决选的选手，将获得组委会颁发的数额不等的奖金，个人所得税自理。 <br>
12、选手必须接受组委会的安排参加组委会举办的颁奖晚会等活动。 <br>
13、选手应对在活动中知悉但未经组委会公开的内容保密。 <br>
14、选手如被取消参赛资格的，除不能获得任何奖励外，还应向组委会退还其所获得的服装、经费等。如选手擅自中途退出活动，除不能获得任何奖励、退还其所获得的服装、经费等外，还应赔偿给组委会造成的损失。 <br>
15、在活动过程中，全程的一切内容及其发展变化所涉及到的知识产权、肖像权等权利，活动组委会均依法享有，并可授予第三方合理使用。 <br>
16、选手如有任何违反选手规则、有损活动及组委会声誉的行为，组委会将有权取消选手参赛资格，并要求选手赔偿由此给组委会造成的损失。<br>
17、活动主办方作为选手的经纪人，独家享有获奖选手两年的经纪权，获奖选手应服从活动主办方的工作及活动安排。活动组委会拥有选手的形象代言权，选手应接受主办方的安排担任相关单位的形象代言人。在活动结束后的三个月内，主办方视具体情况与选手书面确认或解除对选手的独家经纪权。 <br>
18、活动组委会对本选手规则享有最终的解释权。 <br>
19. 如果选手为18周岁以下未成年人，选手及选手的监护人在已阅读并承诺以上全部条款的同时，还需阅读并承诺以下内容： <br>
（1）、选手报名参加活动，已经选手的监护人同意。 <br>
（2）、在活动过程中，除组委会原因外，选手的监护人应对选手的全部行为承担责任； <br>
（3）、在活动过程中，除组委会要求不陪同外，选手的监护人应全程陪同；如不能陪同者，应向组委会提出书面请求，并对在组委会尽合理义务后选手及选手对第三人发生的事故承担全部责任。 <br>
（4）、选手及选手的监护人应保证不因参加活动影响选手的正常学习；选手的监护人应与选手所在学校、社区等取得联系，防止选手因参加活动出现旷课、逃学等影响学业的不当行为，如出现该行为，选手的监护人应对此承担全部责任。<em><br>
